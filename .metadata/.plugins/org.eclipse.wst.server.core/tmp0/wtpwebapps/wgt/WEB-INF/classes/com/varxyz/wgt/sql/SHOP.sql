CREATE TABLE shop (
	BUSINESS_NUMBER		VARCHAR(13) 	PRIMARY KEY,
	SHOP_NAME 			VARCHAR(50) 	NOT NULL,
	SHOP_TEL			VARCHAR(13)		NOT NULL,
	SHOP_POSTCODE		VARCHAR(50)		NOT NULL,
	SHOP_ADDRESS		VARCHAR(50)		NOT NULL,
	SHOP_DETAILADDRESS	VARCHAR(50)		NOT NULL,
	SHOP_EXTRAADDRESS	VARCHAR(50)		DEFAULT '없음',
	SHOP_HOURS			VARCHAR(11)		NOT NULL,
	SHOP_TABLES			VARCHAR(2)		NOT NULL,
	SHOP_MAX_PEOPLES	VARCHAR(2)		NOT NULL,
	SHOP_IMG			VARCHAR(10)		NOT NULL DEFAULT 'default'
);

CREATE TABLE menu (
	BUSINESS_NUMBER		VARCHAR(13) 	NOT NULL,
	MENU_NAME			VARCHAR(20)		NOT NULL DEFAULT '-',
	MENU_INTRO			VARCHAR(100)	NOT NULL DEFAULT '-',
	MENU_PRICE			INT(6)			NOT NULL DEFAULT 0,
	MENU_IMG			VARCHAR(10)		NOT NULL DEFAULT 'default',
	CONSTRAINT Shop_Name_FK
	FOREIGN KEY(BUSINESS_NUMBER) REFERENCES shop(BUSINESS_NUMBER)
);

